void	draw_player(t_img *img, int cursor, int id, BOOL end)
{
	int			i;
	int			x;
	const int	*player;

	i = 0;
	x = 0;
	player = get_player_sprite(id, end);
	while (i < 2304)
	{
		img->addr[cursor] = player[i];
		cursor++;
		i++;
		x++;
		if (x == 48)
		{
			cursor -= x;
			cursor = next_line(img, cursor);
			x = 0;
		}
	}
}
